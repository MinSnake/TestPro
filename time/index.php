//一年的第一天   xxxx年1月1日
$first_day_date = date('Y-01-01 00:00:00', time());
//第一天的时间戳
$first_day_time = strtotime($first_day_date);

//时间跨度
$coder_day_time = 60 * 60 * 24 *256;

$final_time = $first_day_time + $coder_day_time;


echo '今年的程序员日是：' . date('Y-m-d H:i:s', $final_time);

echo '<br>';

