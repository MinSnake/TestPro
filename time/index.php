 * 程序员节是一个国际上被众多科技公司和软件企业承认的业内人士节日。
 * 日期是在每年的第256（十六进制为0x100，或28）天，也就是平年的9月13日或闰年的9月12日。
 *
 * 它是俄罗斯的一个官方节日，其他国家的程序员社区也庆祝这个节日[1]。
 * 之所以选择256（28），是因为它是一个被程序员们所熟知的8比特基本数字。
 *
 * 用1个字节（等于8比特）最多能表示256个数值，
 * 而且在平年中，256是2的最大幂中小于365的值。
